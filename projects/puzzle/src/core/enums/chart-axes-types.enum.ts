/**
 * 图表坐标轴类型枚举
 * 定义不同类型的坐标轴，适用于各种图表（如折线图、柱状图、雷达图等）
 */
export enum ChartAxesTypesEnum {
  /**
   * 序数时间轴
   * - 适用于时间数据，但按离散的序数（如分类）处理
   * - 例如：`["2023-01", "2023-02", ...]`
   */
  ordinalTime = 'ordinal-time',

  /**
   * 常规数值轴
   * - 适用于连续的数值数据（如温度、销售额）
   * - 默认的线性刻度
   */
  number = 'number',

  /**
   * 对数轴
   * - 适用于数值范围跨度极大的数据（如指数增长）
   * - 刻度按对数比例计算（如 1, 10, 100, 1000）
   */
  log = 'log',

  /**
   * 分类轴
   * - 适用于离散的类别数据（如产品名称、城市）
   * - 例如：`["苹果", "香蕉", "橙子"]`
   */
  category = 'category',

  /**
   * 分组分类轴
   * - 适用于嵌套分类数据（如多级目录）
   * - 例如：`["水果/苹果", "水果/香蕉", "蔬菜/胡萝卜"]`
   */
  groupedCategory = 'grouped-category',

  /**
   * 时间轴
   * - 适用于连续时间数据（如时间序列）
   * - 自动处理时间格式（如 `Date` 对象或时间戳）
   */
  time = 'time',

  /**
   * 极坐标角度轴（分类）
   * - 适用于雷达图/极坐标图的分类角度
   * - 例如：将分类均匀分布在圆周上
   */
  angleCategory = 'angle-category',

  /**
   * 极坐标角度轴（数值）
   * - 适用于雷达图/极坐标图的数值角度
   * - 例如：用数值控制角度位置（0°~360°）
   */
  angleNumber = 'angle-number',

  /**
   * 极坐标半径轴（分类）
   * - 适用于雷达图的分层半径（离散值）
   * - 例如：`["内圈", "中圈", "外圈"]`
   */
  radiusCategory = 'radius-category',

  /**
   * 极坐标半径轴（数值）
   * - 适用于雷达图的连续半径（如数值大小）
   * - 例如：数值越大，离中心越远
   */
  radiusNumber = 'radius-number'
}
